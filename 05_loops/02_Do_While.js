// While

let i = 1;

while (i <= 10) {
  //   console.log(`Value of i ${i}`);
  i = i + 1;
}

let myArray = ["flash", "batman", "superman"];
let arr = 0;
while (arr < myArray.length) {
  //   console.log(`value is ${myArray[arr]}`);
  arr = arr + 1;
}

// Do While

let score = 11;

do {
  //   console.log(`score is ${score}`);
  score = score + 1;
} while (score <= 10);
